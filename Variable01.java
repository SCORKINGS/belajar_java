public class Variable01 {

    public static void main(String[] args) {
        // deklaraasi var
        String nama;
        int umur;
        // input nilai

        nama = "Najwan";
        
        // output ke layar
        System.out.println("Nama: " + nama);

        nama = "Fahraya Ramdhiya";
        System.out.println("Nama: " + nama);

        
        umur = 18;
        System.out.println("Umur: " + umur);
    }
}